def find_disease_group(disease_name: str) -> str:
    """
    질환명을 받아서 어떤 질환 그룹에 속하는지 반환한다.
    1. 정확한 매칭 확인
    2. 키워드 기반 부분 매칭 확인
    없으면 '기타' 반환.
    """
    if not disease_name:
        return "기타"

    # 1. Exact Match
    for group_name, disease_list in DISEASE_TAXONOMY.items():
        if disease_name in disease_list:
            return group_name

    # 2. Keyword Match (Smarter Category)
    for group_name, keywords in GROUP_KEYWORDS.items():
        for kw in keywords:
            if kw in disease_name:
                return group_name

    return "기타"


def is_known_disease(disease_name: str) -> bool:
    """
    taxonomy에 등록된 표준 질환인지 확인한다.
    """
    if not disease_name:
        return False
    for disease_list in DISEASE_TAXONOMY.values():
        if disease_name in disease_list:
            return True
    return False
